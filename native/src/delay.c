#include "stm8s.h"
#include "delay.h"

void delay_us(uint16_t cnt)
{
    while(cnt--)
    {
        nop();
        nop();
        nop();
    }
}

void delay_ms(uint16_t cnt)
{
    while(cnt--)
    {
        delay_us(999);
    }
}

